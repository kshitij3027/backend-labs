@pytest.mark.asyncio
async def test_audit_entry_unique_entry_hash(session_factory):
    """Two ``AuditEntry`` rows with the same ``entry_hash`` raise IntegrityError."""
    common_hash = "a" * 64
    async with session_factory() as session:
        e1 = AuditEntry(
            seq=1,
            ts_utc=datetime(2026, 1, 1, 0, 0, 0),
            actor="system",
            action="test",
            resource="r1",
            metadata_json="{}",
            prev_hash="0" * 64,
            entry_hash=common_hash,
        )
        session.add(e1)
        await session.commit()

    with pytest.raises(IntegrityError):
        async with session_factory() as session:
            e2 = AuditEntry(
                seq=2,
                ts_utc=datetime(2026, 1, 1, 0, 0, 1),
                actor="system",
                action="test",
                resource="r2",
                metadata_json="{}",
                prev_hash=common_hash,  # would normally chain off e1
                entry_hash=common_hash,  # duplicate — must fail
            )
            session.add(e2)
            await session.commit()


