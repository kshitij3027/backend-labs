@pytest.mark.asyncio
async def test_job_run_round_trip(session_factory):
    """``JobRun`` round-trips; ``finished_at`` and ``summary_json`` are nullable."""
    started = datetime(2026, 1, 1, 0, 0, 0)
    finished = started + timedelta(seconds=2)
    async with session_factory() as session:
        # Pending run (no finish yet).
        r1 = JobRun(job_name="scan", started_at=started, status="running")
        session.add(r1)
        await session.commit()
        r1_id = r1.id

        # Completed run.
        r2 = JobRun(
            job_name="apply",
            started_at=started,
            finished_at=finished,
            status="ok",
            summary_json='{"applied":3}',
        )
        session.add(r2)
        await session.commit()
        r2_id = r2.id

    async with session_factory() as session:
        running = (
            await session.execute(sa.select(JobRun).where(JobRun.id == r1_id))
        ).scalar_one()
        done = (
            await session.execute(sa.select(JobRun).where(JobRun.id == r2_id))
        ).scalar_one()
    assert running.job_name == "scan"
    assert running.finished_at is None
    assert running.summary_json is None
    assert running.status == "running"
    assert done.finished_at == finished
    assert done.summary_json == '{"applied":3}'
    assert done.status == "ok"
