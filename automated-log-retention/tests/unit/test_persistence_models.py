"""Unit tests for ``src/persistence/models.py``.

These cover three layers:

  1. Schema metadata — table names, column presence, key indexes.
  2. Round-trip — insert + fetch each model through the in-memory
     ``session_factory`` fixture defined in ``tests/conftest.py``.
  3. Constraints — the audit chain's ``entry_hash`` UNIQUE rule
     actually surfaces ``IntegrityError`` on a duplicate insert.
"""
from __future__ import annotations

from datetime import datetime, timedelta

import pytest
import sqlalchemy as sa
from sqlalchemy.exc import IntegrityError

from src.persistence.models import (
    AuditEntry,
    Base,
    File,
    JobRun,
    PendingDelete,
    Transition,
)


# --- Static schema checks ----------------------------------------------------


def test_all_models_exported():
    """The five ORM classes plus Base must be importable from the module."""
    # Smoke check — if the import at the top failed, the test wouldn't even
    # collect; this just makes the contract explicit.
    assert Base is not None
    for cls in (File, Transition, PendingDelete, AuditEntry, JobRun):
        assert cls.__tablename__ in {
            "files",
            "transitions",
            "pending_deletes",
            "audit_entries",
            "job_runs",
        }


def test_file_columns():
    cols = set(File.__table__.columns.keys())
    expected = {
        "id",
        "source",
        "category",
        "segment_path",
        "tier",
        "size_bytes",
        "oldest_record_ts",
        "newest_record_ts",
        "compliance_tag",
        "immutable",
        "next_eval_at",
        "created_at",
    }
    assert cols == expected, f"missing={expected - cols} extra={cols - expected}"
    assert File.__table__.columns["segment_path"].unique is True
    # ``category`` is a nullable indexed string; the scanner reads it to
    # match policies, so a missing column would silently break the demo.
    assert File.__table__.columns["category"].nullable is True


