# --- Indexes -----------------------------------------------------------------


@pytest.mark.asyncio
async def test_indexes_exist(engine):
    """All required indexes are created on the live SQLite schema."""
    async with engine.connect() as conn:
        result = await conn.exec_driver_sql(
            "SELECT name, tbl_name FROM sqlite_master WHERE type='index';"
        )
        rows = result.fetchall()
    # We can't pin index names for SQLAlchemy-autogenerated ones (they look
    # like ix_files_tier), so we check tbl_name + the columns covered.
    index_by_name = {name: tbl for name, tbl in rows}
    # SQLAlchemy auto-generates names of the form ix_<table>_<column>.
    assert "ix_files_tier" in index_by_name
    assert "ix_files_next_eval_at" in index_by_name
    assert "ix_files_source" in index_by_name
    assert "ix_files_category" in index_by_name
    assert "ix_transitions_status" in index_by_name
    assert "ix_transitions_file_id" in index_by_name
    assert "ix_pending_deletes_delete_after" in index_by_name
    assert "ix_audit_entries_actor_ts" in index_by_name  # composite
    assert "ix_job_runs_job_name" in index_by_name


@pytest.mark.asyncio
async def test_audit_entries_composite_index_columns(engine):
    """The composite index is on (actor, ts_utc) in that order."""
    async with engine.connect() as conn:
        result = await conn.exec_driver_sql(
            "PRAGMA index_info('ix_audit_entries_actor_ts');"
        )
        rows = result.fetchall()
    # PRAGMA index_info returns (seqno, cid, name) — sort by seqno.
    col_names = [row[2] for row in sorted(rows, key=lambda r: r[0])]
    assert col_names == ["actor", "ts_utc"]


# --- Round-trip tests --------------------------------------------------------


@pytest.mark.asyncio
async def test_file_round_trip(session_factory):
    """Insert a ``File`` row; fetch it back; every field matches."""
    oldest = datetime(2026, 1, 1, 0, 0, 0)
    newest = datetime(2026, 1, 1, 12, 0, 0)
    next_eval = datetime(2026, 1, 2, 0, 0, 0)
    async with session_factory() as session:
        f = File(
            source="app-1",
            category="payment",
            segment_path="/tiers/hot/segment-1.jsonl",
            tier="hot",
            size_bytes=2048,
            oldest_record_ts=oldest,
            newest_record_ts=newest,
            compliance_tag="sox",
            immutable=False,
            next_eval_at=next_eval,
        )
        session.add(f)
        await session.commit()
        file_id = f.id

    async with session_factory() as session:
        result = await session.execute(sa.select(File).where(File.id == file_id))
        fetched = result.scalar_one()
    assert fetched.id == file_id
    assert fetched.source == "app-1"
    assert fetched.category == "payment"
    assert fetched.segment_path == "/tiers/hot/segment-1.jsonl"
    assert fetched.tier == "hot"
    assert fetched.size_bytes == 2048
    assert fetched.oldest_record_ts == oldest
    assert fetched.newest_record_ts == newest
    assert fetched.compliance_tag == "sox"
    assert fetched.immutable is False
    assert fetched.next_eval_at == next_eval
    assert fetched.created_at is not None


@pytest.mark.asyncio
async def test_transition_foreign_key(session_factory):
    """Inserting a Transition that references a real File round-trips both."""
    async with session_factory() as session:
        f = File(
            source="app-2",
            segment_path="/tiers/hot/segment-2.jsonl",
            tier="hot",
            size_bytes=1024,
            oldest_record_ts=datetime(2026, 1, 1),
            newest_record_ts=datetime(2026, 1, 1),
        )
        session.add(f)
        await session.flush()  # get f.id without committing
        t = Transition(
            file_id=f.id,
            from_tier="hot",
            to_tier="warm",
            action="promote",
            status="pending",
        )
        session.add(t)
        await session.commit()
        file_id, transition_id = f.id, t.id

    async with session_factory() as session:
        file_row = (
            await session.execute(sa.select(File).where(File.id == file_id))
        ).scalar_one()
        tx_row = (
            await session.execute(
                sa.select(Transition).where(Transition.id == transition_id)
            )
        ).scalar_one()
    assert tx_row.file_id == file_row.id
    assert tx_row.from_tier == "hot"
    assert tx_row.to_tier == "warm"
    assert tx_row.action == "promote"
    assert tx_row.status == "pending"
    assert tx_row.error is None
    assert tx_row.executed_at is None
    assert tx_row.planned_at is not None


